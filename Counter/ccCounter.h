//******************************************************************************
//******************************************************************************
//******************************************************************************
// This class implements a counter modulo M.
// 

class CounterN
{
public:

   //***************************************************************************
   //***************************************************************************
   //***************************************************************************
   // Members.

   // Modulus.
   int mDivisor;

   // Counter value.
   int mValue;

   // The results of a division of Z by N are a quotient and a remainder
   // Z = Q*N + R
   // Z is an element of [0..M-1]
   // Q is an element of [0..(M/N)-1]
   // R is an element of [0..N-1]

   int mQuotient;
   int mRemainder;

   //***************************************************************************
   //***************************************************************************
   //***************************************************************************
   // Methods.

   // Constructor,
   CounterN();
   CounterN(int aDivisor);
   void reset();
   void initialize(int aDivisor);

   // Set value.
   void set(int aValue);
   void increment();
   bool decrement();

};

