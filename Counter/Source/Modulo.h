#ifndef _QUOTIENTGROUP_H_
#define _QUOTIENTGROUP_H_
/*==============================================================================

This defines a class template that is used to define classes that encapsulate
integer quotient groups.

==============================================================================*/

//******************************************************************************
//******************************************************************************
//******************************************************************************

//******************************************************************************
//******************************************************************************
//******************************************************************************

template <int N>
class QuotientGroupZPlusOverN
{
public:
   //---------------------------------------------------------------------------
   // Methods

   // Constructor
   QuotientGroupZPlusOverN()
   {
      mQuotient      = 0;
      mRemainder     = 0;
   }


   void putG(int aElementOfG)
   {
      mQuotient  = aElementOfG / N;
      mRemainder = aElementOfG % N;
   }

   //---------------------------------------------------------------------------
   // Members

   int      mQuotient;
   int      mRemainder;
};

//******************************************************************************
//******************************************************************************
//******************************************************************************

template <int G,int N>
class QuotientGroupGOverN
{
public:
   //---------------------------------------------------------------------------
   // Methods

   // Constructor
   QuotientGroupGOverN()
   {
      mQuotient      = 0;
      mRemainder     = 0;
   }


   void putG(int aElementOfG)
   {
      mQuotient  = aElementOfG / N;
      mRemainder = aElementOfG % N;
   }

   //---------------------------------------------------------------------------
   // Members

   int      mQuotient;
   int      mRemainder;
};

//******************************************************************************


#endif

