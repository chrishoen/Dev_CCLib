#ifndef _SOMEMESSAGE_H_
#define _SOMEMESSAGE_H_

/*==============================================================================
==============================================================================*/

#include <stdio.h>

//******************************************************************************
//******************************************************************************
//******************************************************************************


namespace Some
{

//******************************************************************************
//******************************************************************************
//******************************************************************************
#pragma pack( push, PACK_SOMEMESSAGE,1 )

class IntMessage
{
public:

   //--------------------------------------------------------------------------
   // Members

   unsigned short mIdent;
   unsigned short mCode;

   //--------------------------------------------------------------------------
   // Methods

   IntMessage()
   {
      mIdent=0;
      mCode=0;
   }

   IntMessage(unsigned short aIdent,unsigned short aCode)
   {
      mIdent = aIdent;
      mCode  = aCode;
   }

   int* ptr() { return (int*)&mIdent; }

   int& aint() { return *(int*)&mIdent; }
};

//******************************************************************************
}//namespace

#pragma pack( pop, PACK_SOMEMESSAGE )

#endif

