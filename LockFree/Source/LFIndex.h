#pragma pack( push, PACK_LFINDEX,1 )

class LFIndex
{
public:

   unsigned mIndex;
   unsigned mCount;

   LFIndex()
   {
      mIndex=0;
      mCount=0;
   }

   LFIndex(unsigned aIndex,unsigned aCount)
   {
      mIndex = aIndex;
      mCount = aCount;
   }
};

#pragma pack( pop, PACK_LFINDEX )

