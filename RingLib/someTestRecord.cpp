// Set and test.

// No constructor.
void TestRecord::reset()
{
   for (int i = 0; i < cNumCodes; i++) mCode[i] = 0;
}

// Set.
void TestRecord::doSet(long long aCode)
{
   for (int i = 0; i < cNumCodes; i++) mCode[i] = aCode;
}

// Test.
bool TestRecord::doTest(long long aCode)
{
   bool tPass = true;
   for (int i = 0; i < cNumCodes; i++) if (mCode[i] != aCode) tPass = false;
   return tPass;
}

